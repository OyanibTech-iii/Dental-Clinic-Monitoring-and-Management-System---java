    private void initializePasswordField() {

        passwordTxt1.setText("Password");
        passwordTxt1.setEchoChar((char) 0);

        // Add focus listener to handle placeholder behavior with password masking
        passwordTxt1.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent evt) {
                if (new String(passwordTxt1.getPassword()).equals("Password")) {
                    passwordTxt1.setText("");
                    // Set echo char based on current visibility state
                    if (!isPasswordVisible) {
                        passwordTxt1.setEchoChar('*');
                    } else {
                        passwordTxt1.setEchoChar((char) 0);
                    }
                }
            }
        });
    }

