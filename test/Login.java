    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        MouseClick.playClickSound();
        String username = usernameTxt.getText();
        String password = passwordTxt1.getText();

        // Basic validation
        if (username.isEmpty() || username.equals("Username")
                || password.isEmpty() || password.equals("Password")) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields", "Error", JOptionPane.ERROR_MESSAGE);
            MouseClick.playClickSound();
            return;
        }

        try {
            // Get database connection
            Connection conn = DbConnection.connectToDb();
            if (conn == null) {
                JOptionPane.showMessageDialog(null, "Failed to connect to database", "Error", JOptionPane.ERROR_MESSAGE);
                MouseClick.playClickSound();
                return;
            }

            // Prepare SQL statement to check user credentials
            String sql = "SELECT * FROM users WHERE username = ? AND password = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, username);
            pstmt.setString(2, password);

            // Execute query
            ResultSet rs = (ResultSet) pstmt.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Log in Successful");
                // Clear fields after successful login
                usernameTxt.setText("Username");
                passwordTxt1.setText("Password");
                MouseClick.playClickSound();
                // Navigate to LandingPage
                LandingPage land = new LandingPage(username);
                land.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Invalid username or password", "Error", JOptionPane.ERROR_MESSAGE);
                MouseClick.playClickSound();
            }

            // Close resources
            rs.close();
            pstmt.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            MouseClick.playClickSound();
        }


    }//GEN-LAST:event_loginBtnActionPerformed

    private void hideShwIconMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hideShwIconMouseClicked
        // TODO add your handling code here:
        if (!new String(passwordTxt1.getPassword()).equals("Password")) {
            if (isPasswordVisible) {
                // Hide password - show asterisks
                passwordTxt1.setEchoChar('*');
                hideShwIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/unhidden.png")));
                isPasswordVisible = false;
            } else {
                // Show password - display plain text
                passwordTxt1.setEchoChar((char) 0); // 0 means no echo character (plain text)
                hideShwIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/hidden.png")));
                isPasswordVisible = true;
            }
        }
    }//GEN-LAST:event_hideShwIconMouseClicked

